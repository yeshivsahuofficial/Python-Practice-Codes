# Set is unordered data structure i.e., no index value
# It does not allow "Duplicate" values
# Sets support various operations like:
#     1. union (|),
#     2. intersection (&),
#     3. difference (-),
#     4. symmetric difference (^).

set1 =  {45,45,645,53,75,43,85}
print(set1)

#Iterating set elements
for x in set1:
    print(x,end="  ")
print()

