# There are two membership operators: in and not in.
# It checks if a value is present (or absent) within sequences like strings, lists,etc.

#"in": Checks if a value exists in a sequence.
    # Example with string
print('H' in 'Hello')  # True

    # Example with List
print(3 in [1, 2, 3, 4, 5])  # True


# not in: Checks if a value does not exist in a sequence.
    # Example with string
print('h' not in 'Hello')  # True

    # Example with list
print(6 not in [1, 2, 3, 4, 5])  # True
