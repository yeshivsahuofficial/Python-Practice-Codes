#Strings are indexed
str1="Yeshiv Sahu"
print(str1)

#Length function
print(len(str1))

# 'in' function
print("shiv" in str1)

#Accessing element via index number
print(str1[0],end="")
print(str1[1],end="")
print(str1[6],end="")
print(str1[7])

#Slicing
print(str1[0:6])    #Yeshiv
print(str1[5:8])
print(str1[::-1])    #Reverse


