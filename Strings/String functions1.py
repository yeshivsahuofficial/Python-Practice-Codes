#Upper()  Capitalize all characters
str1="Yeshiv Sahu"
print(str1.upper())

#Lower() De-capitalize all characters
str2="YESHIV SAHU"
print(str2.lower())

#Capitalize() Capitalizes the first character
str3="yeshiv sahu"
print(str3.capitalize())

#title() Capitalizes every character after whitespace
str4="yeshiv sahu"
print(str4.title())
