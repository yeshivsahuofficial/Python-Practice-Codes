marks=(67,85,34,98,56)
print(marks)

print(f"Max of marks :{max(marks)}")   #max()

print(f"Min of marks: {min(marks)}")   #min()

print(f"Occurence of 35 in marks {marks.count(35)}")  #tuple.count(element)   counts occurences of element
print(f"Occurence of 35 in marks {marks.count(98)}")

print(f"Sum of marks {sum(marks)}")   #sum()

print(f"Index number of 85 in marks: {marks.index(85)}")  #Gives index no. of element

